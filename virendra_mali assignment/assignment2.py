# function program for mathematical operations
# addition
def add(x,y):
    return x+y
print(add(3,7))

# subtraction
def sub(x):
    return 50-x
print(sub(10))

# multiplication
def multi(x, y, z):
    return x*y*z
print(multi(2, 4, 5))

# division
def div(a):
    return 100/a
print(div(50))

# floor division
def fdiv(x):
    return 50//x
print(fdiv(7))

# modulus
def mod(x, y):
    return x%y
print(mod(10, 3))

# exponentiation
def expo(a, b):
    return a**b
print(expo(2, 3))