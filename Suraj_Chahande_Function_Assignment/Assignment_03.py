# Write a program for mathematical operations in a single function.

def Math():
    num1 = int(input("Enter first number: "))
    num2 = int(input("Enter second number: "))
    add = num1 + num2
    print("The additon of two numbers is: ",add)
    sub = num1 - num2
    print("The substraction of two numbers is: ",sub)
    mul = num1 * num2
    print("The multiplication of two numbers is: ",mul)
    div = num1 / num2
    print("The division of two numbers is: ",div)
    
Math()
    